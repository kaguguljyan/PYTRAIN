# переменные
x = 5  # int
y = 1.2  # float
z = "Hello"  # str
s = True  # bool

# операции над переменными
# арифметические операции
a = x + y
b = x * y
c = x - y
d = x // y
# сравнительные операции
e = x > y
f = x == 3
# конкатенация
g = z + " " + str(x)

# вывод типа исходных переменных и результата
print(type(x), type(y), type(z), type(s))
print(f"a = {a}, {type(a)}")
print(f"b = {b}, {type(b)}")
print(f"c = {c}, {type(c)}")
print(f"d = {d}, {type(d)}")
print(f"e = {e}, {type(e)}")
print(f"f = {f}, {type(f)}")
print(f"g = {g}, {type(g)}")
